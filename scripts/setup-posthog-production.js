#!/usr/bin/env node
/**
 * PostHog production setup: separate dev/prod projects, push env to Render/Vercel, smoke test capture.
 *
 * Prerequisites (gitignored):
 *   .cursor/posthog.local.env       — POSTHOG_PERSONAL_API_KEY=phx_...
 *   .cursor/production-hosts.local.json
 *   .cursor/render-api.local.env    — optional, for server env
 *   .cursor/vercel-api.local.env    — optional, for client env
 *
 * Usage:
 *   node scripts/setup-posthog-production.js --dry-run
 *   node scripts/setup-posthog-production.js --all
 *   node scripts/setup-posthog-production.js --provision --push --test
 */
const fs = require('fs');
const path = require('path');
const { execSync } = require('child_process');

const ROOT = path.join(__dirname, '..');
const POSTHOG_ENV_PATH = path.join(ROOT, '.cursor', 'posthog.local.env');
const HOSTS_PATH = path.join(ROOT, '.cursor', 'production-hosts.local.json');

const PROD_PROJECT_NAME = 'CoreKnot Production';
const DEV_PROJECT_NAME = 'CoreKnot Development';
const TEST_EVENT = 'coreknot_posthog_setup_ping';

const args = new Set(process.argv.slice(2));
const dryRun = args.has('--dry-run');
const runAll = args.has('--all');
const runProvision = runAll || args.has('--provision');
const runPush = runAll || args.has('--push');
const runTest = runAll || args.has('--test');

const log = (msg) => console.log(msg);
const ok = (msg) => log(`  ✓ ${msg}`);
const warn = (msg) => log(`  ⚠ ${msg}`);
const fail = (msg) => log(`  ✗ ${msg}`);

function loadDotEnv(filePath) {
  if (!fs.existsSync(filePath)) return {};
  const out = {};
  for (const line of fs.readFileSync(filePath, 'utf8').split(/\r?\n/)) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith('#')) continue;
    const eq = trimmed.indexOf('=');
    if (eq < 0) continue;
    const key = trimmed.slice(0, eq).trim();
    let val = trimmed.slice(eq + 1).trim();
    if ((val.startsWith('"') && val.endsWith('"')) || (val.startsWith("'") && val.endsWith("'"))) {
      val = val.slice(1, -1);
    }
    out[key] = val;
  }
  return out;
}

function writeDotEnv(filePath, entries) {
  const lines = [
    '# Auto-generated by scripts/setup-posthog-production.js — gitignored',
    '# Production phc_ token → Vercel Production + Render',
    '# Development phc_ token → local client/.env only',
    '',
  ];
  for (const [key, value] of Object.entries(entries)) {
    if (value) lines.push(`${key}=${value}`);
  }
  lines.push('');
  if (dryRun) {
    ok(`Would write ${path.relative(ROOT, filePath)}`);
    return;
  }
  fs.mkdirSync(path.dirname(filePath), { recursive: true });
  fs.writeFileSync(filePath, lines.join('\n'), 'utf8');
  ok(`Wrote ${path.relative(ROOT, filePath)}`);
}

function resolveAppHost(host = '') {
  const region = String(host).toLowerCase().includes('eu') ? 'eu' : 'us';
  return region === 'eu' ? 'https://eu.posthog.com' : 'https://us.posthog.com';
}

function resolveIngestHost(host = '') {
  const region = String(host).toLowerCase().includes('eu') ? 'eu' : 'us';
  return region === 'eu' ? 'https://eu.i.posthog.com' : 'https://us.i.posthog.com';
}

async function posthogFetch(personalKey, route, options = {}) {
  const host = resolveAppHost(process.env.POSTHOG_HOST || 'https://us.i.posthog.com');
  const res = await fetch(`${host}${route}`, {
    ...options,
    headers: {
      Accept: 'application/json',
      'Content-Type': 'application/json',
      Authorization: `Bearer ${personalKey}`,
      ...(options.headers || {}),
    },
  });
  const text = await res.text();
  let payload = null;
  if (text) {
    try { payload = JSON.parse(text); } catch { payload = text; }
  }
  if (!res.ok) {
    const detail = payload?.detail || payload?.message || (typeof payload === 'string' ? payload : JSON.stringify(payload));
    throw new Error(`${options.method || 'GET'} ${route} → ${res.status}: ${detail}`);
  }
  return payload;
}

async function getOrganizationId(personalKey) {
  const me = await posthogFetch(personalKey, '/api/users/@me/');
  const orgs = me?.organizations || [];
  if (!orgs.length) throw new Error('No PostHog organizations on this account');
  return orgs[0].id;
}

async function listProjects(personalKey, orgId) {
  const data = await posthogFetch(personalKey, `/api/organizations/${orgId}/projects/`);
  return data?.results || data || [];
}

async function createProject(personalKey, orgId, name) {
  return posthogFetch(personalKey, `/api/organizations/${orgId}/projects/`, {
    method: 'POST',
    body: JSON.stringify({ name }),
  });
}

async function findOrCreateProject(personalKey, orgId, name) {
  const projects = await listProjects(personalKey, orgId);
  const existing = projects.find((p) => String(p.name).toLowerCase() === name.toLowerCase());
  if (existing) return existing;
  if (dryRun) {
    ok(`Would create PostHog project "${name}"`);
    return { name, id: 'dry-run', api_token: 'phc_dry_run' };
  }
  const created = await createProject(personalKey, orgId, name);
  ok(`Created PostHog project "${name}" (${created.id})`);
  return created;
}

async function sendTestEvent(projectToken, host, distinctId = 'coreknot-setup') {
  const ingest = resolveIngestHost(host);
  const body = {
    api_key: projectToken,
    event: TEST_EVENT,
    distinct_id: distinctId,
    properties: {
      source: 'setup-posthog-production.js',
      environment: 'production-setup',
      timestamp: new Date().toISOString(),
    },
  };
  if (dryRun) {
    ok(`Would send test event "${TEST_EVENT}" to ${ingest}/capture/`);
    return true;
  }
  const res = await fetch(`${ingest}/capture/`, {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify(body),
  });
  if (!res.ok) {
    const text = await res.text();
    throw new Error(`Test capture failed → ${res.status}: ${text}`);
  }
  ok(`Test event "${TEST_EVENT}" sent`);
  return true;
}

function readFrontendUrl() {
  if (!fs.existsSync(HOSTS_PATH)) return 'https://tsccoreknot.com';
  try {
    const hosts = JSON.parse(fs.readFileSync(HOSTS_PATH, 'utf8'));
    return String(hosts.productionFrontendUrl || 'https://tsccoreknot.com').replace(/\/$/, '');
  } catch {
    return 'https://tsccoreknot.com';
  }
}

async function stepProvision(personalKey, posthogHost) {
  log('\n=== 1. PostHog projects (dev + production) ===\n');
  const orgId = await getOrganizationId(personalKey);
  ok(`Organization ${orgId}`);

  const prod = await findOrCreateProject(personalKey, orgId, PROD_PROJECT_NAME);
  const dev = await findOrCreateProject(personalKey, orgId, DEV_PROJECT_NAME);

  const prodToken = prod.api_token || prod.project_api_key;
  const devToken = dev.api_token || dev.project_api_key;
  if (!prodToken || !devToken) {
    throw new Error('Could not read project api_token from PostHog API response');
  }

  const appHost = resolveAppHost(posthogHost);
  const envEntries = {
    POSTHOG_PERSONAL_API_KEY: personalKey,
    POSTHOG_HOST: posthogHost,
    VITE_POSTHOG_HOST: posthogHost,
    VITE_POSTHOG_PROJECT_TOKEN: prodToken,
    VITE_POSTHOG_PROJECT_TOKEN_DEV: devToken,
    POSTHOG_PROJECT_API_KEY: prodToken,
    VITE_POSTHOG_PROJECT_ID: String(prod.id),
    VITE_POSTHOG_DASHBOARD_URL: `${appHost}/project/${prod.id}`,
    VITE_POSTHOG_APP_URL: readFrontendUrl(),
  };

  writeDotEnv(POSTHOG_ENV_PATH, envEntries);

  ok(`Production project: ${prod.name} (${prod.id})`);
  ok(`Development project: ${dev.name} (${dev.id})`);
  ok(`Dashboard: ${envEntries.VITE_POSTHOG_DASHBOARD_URL}`);

  return envEntries;
}

function stepPushLocalDevToken(devToken) {
  log('\n=== 2. Local dev token (client/.env) ===\n');
  const clientEnv = path.join(ROOT, 'client', '.env');
  const lines = fs.existsSync(clientEnv) ? fs.readFileSync(clientEnv, 'utf8').split(/\r?\n/) : [];
  const key = 'VITE_POSTHOG_PROJECT_TOKEN';
  const nextLine = `${key}=${devToken}`;
  let replaced = false;
  const out = lines.map((line) => {
    if (line.startsWith(`${key}=`)) {
      replaced = true;
      return nextLine;
    }
    return line;
  });
  if (!replaced) out.push(nextLine);
  if (!out.some((l) => l.startsWith('VITE_POSTHOG_HOST='))) {
    out.push('VITE_POSTHOG_HOST=https://us.i.posthog.com');
  }
  if (dryRun) {
    ok(`Would set ${key} in client/.env (dev project)`);
    return;
  }
  fs.writeFileSync(clientEnv, `${out.filter((l, i, arr) => !(i === arr.length - 1 && l === '')).join('\n')}\n`, 'utf8');
  ok('client/.env updated with dev PostHog token');
}

function stepPushPlatforms() {
  log('\n=== 3. Render + Vercel env (production token) ===\n');
  if (dryRun) {
    ok('Would run: node scripts/setup-production-full.js --render --vercel');
    return;
  }
  try {
    execSync('node scripts/setup-production-full.js --render --vercel', {
      cwd: ROOT,
      stdio: 'inherit',
    });
  } catch (err) {
    warn(`Platform push failed: ${err.message}`);
    warn('Ensure .cursor/render-api.local.env and .cursor/vercel-api.local.env exist');
  }
}

async function main() {
  log('\nPostHog production setup');
  if (dryRun) log('(dry-run — no changes)\n');

  const fileEnv = loadDotEnv(POSTHOG_ENV_PATH);
  const personalKey = (
    process.env.POSTHOG_PERSONAL_API_KEY
    || process.env.POSTHOG_API_KEY
    || fileEnv.POSTHOG_PERSONAL_API_KEY
  )?.trim();

  const posthogHost = (
    fileEnv.POSTHOG_HOST
    || process.env.POSTHOG_HOST
    || 'https://us.i.posthog.com'
  ).trim();

  let envEntries = fileEnv;

  if (runProvision) {
    if (!personalKey) {
      fail('POSTHOG_PERSONAL_API_KEY missing');
      log('  Create at https://us.posthog.com/settings/user-api-keys');
      log('  Save to .cursor/posthog.local.env');
      process.exit(1);
    }
    envEntries = await stepProvision(personalKey, posthogHost);
  }

  const prodToken = envEntries.VITE_POSTHOG_PROJECT_TOKEN?.trim();
  const devToken = envEntries.VITE_POSTHOG_PROJECT_TOKEN_DEV?.trim();

  if (runPush) {
    if (devToken) stepPushLocalDevToken(devToken);
    stepPushPlatforms();
  }

  if (runTest) {
    log('\n=== 4. Test event capture ===\n');
    if (!prodToken) {
      fail('No production project token — run with --provision first');
      process.exit(1);
    }
    await sendTestEvent(prodToken, posthogHost);
    log(`  Check PostHog → Live events for "${TEST_EVENT}"`);
    if (envEntries.VITE_POSTHOG_DASHBOARD_URL) {
      log(`  Dashboard: ${envEntries.VITE_POSTHOG_DASHBOARD_URL}`);
    }
  }

  if (!runProvision && !runPush && !runTest) {
    log('Usage: node scripts/setup-posthog-production.js --all');
    log('       node scripts/setup-posthog-production.js --provision --push --test');
  } else {
    log('\nDone. Redeploy Vercel production after env push.\n');
  }
}

main().catch((err) => {
  console.error(`\n✗ ${err.message}\n`);
  process.exit(1);
});
