const BASE = process.env.TEST_API_URL || 'http://127.0.0.1:5000';

async function resolveAdminUserId() {
  const uri = process.env.MONGODB_URI;
  if (!uri) throw new Error('MONGODB_URI not set');
  await mongoose.connect(uri);
  const adminDept = await Department.findOne({ slug: ADMIN_SLUG }).select('_id').lean();
  if (!adminDept) throw new Error('Admin department not found');
  const admin = await User.findOne({ departmentId: adminDept._id }).select('_id').lean();
  if (!admin?._id) throw new Error('Admin user not found');
  return admin._id.toString();
}

async function presign(token, slug, file) {
  const res = await fetch(`${BASE}/api/uploadthing?actionType=upload&slug=${slug}`, {
    method: 'POST',
    headers: {
      Authorization: `Bearer ${token}`,
      'content-type': 'application/json',
      'x-uploadthing-package': 'uploadthing/client',
      'x-uploadthing-version': '7.7.4',
    },
    body: JSON.stringify({
      files: [{
        name: file.name,
        size: file.size,
        type: file.type,
        lastModified: file.lastModified || Date.now(),
      }],
      input: null,
    }),
  });
  const text = await res.text();
  if (!res.ok) throw new Error(`presign ${slug} ${res.status}: ${text}`);
  const data = JSON.parse(text);
  return Array.isArray(data) ? data[0] : data;
}

async function uploadToIngest(presigned, file) {
  const form = new FormData();
  form.append('file', file);
  const res = await fetch(presigned.url, {
    method: 'PUT',
    headers: {
      Range: 'bytes=0-',
      'x-uploadthing-version': '7.7.4',
    },
    body: form,
  });
  const text = await res.text();
  if (!res.ok) throw new Error(`ingest ${res.status}: ${text.slice(0, 300)}`);
  const payload = JSON.parse(text);
  const url = payload.ufsUrl || payload.url;
  if (!url) throw new Error(`ingest missing url: ${text.slice(0, 300)}`);
  return url;
}

async function testUpload(token, slug, file, label) {
  process.stdout.write(`[${label}] ${file.name} via ${slug}... `);
  const presigned = await presign(token, slug, file);
  const url = await uploadToIngest(presigned, file);
  console.log('OK');
  console.log(`  url: ${url}`);
  return url;
}

